# Custom Time Format
from time import *
y=strftime("%Y") # Full year
print(y)
print(strftime("%y")) # last two digits of year
print(strftime("%d"))  # date
print(strftime("%D"))  # full date
print(strftime("%A")) # full day name
print(strftime("%a"))
print(strftime("%B")) # full month name
print(strftime("%b"))
print(strftime("%c"))
print(strftime("%H"))
print(strftime("%M"))
print(strftime("%m"))
print(strftime("%S"))

print(strftime("%d-%b-%y"))
